const KEY = "f84fc31d";

export default function App() {
  const [movies, setMovies] = useState(tempMovieData);
  const [watched, setWatched] = useState(tempWatchedData);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);
  const tempQuery = "interstellar";

  function handleSelectMovie(id) {
    // setSelectedId(id);
    setSelectedId((selectedId) => (id === selectedId ? null : id)); //so that when a diff movie is selected in left box other than the currently selected one, the movie details gets exit from right box
  }
  function handleCloseMovie() {
    setSelectedId(null);
  }

  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function handleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  useEffect(
    function () {
      function callback(e) {
        if (e.code === "Escape") {
          handleCloseMovie();
        }
      }
      document.addEventListener("keydown", callback);
      //cleanup function
      return function () {
        document.removeEventListener("keydown", callback);
      };
    },
    [handleCloseMovie]
  );

  useEffect(
    function () {
      const controller = new AbortController();
      async function fetchMovies() {
        try {
          setIsLoading(true); //loading while fetching data
          setError(""); //always set error to empty string before fetching data
          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
            { signal: controller.signal }
          );

          if (!res.ok) {
            throw new Error("Something went wrong with fetching movies"); //suppose when internet speed is slow or offline
          }

          const data = await res.json();

          if (data.Response === "False") {
            throw new Error("Movie not found");
          }

          setMovies(data.Search);
          setError("");
          console.log(data.Search);
        } catch (err) {
          console.error(err.message);
          if (err.name !== "AbortError") {
            setError(err.message);
          }
        } finally {
          setIsLoading(false);
        }
      }
      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }
      handleCloseMovie();
      fetchMovies();

      //cleanup function for fetching HTTP requests
      return function () {
        controller.abort(); //each time there is a new keystroke in search, the controller will abort the current fetch request and preserve the request which took the longest to load to avoid race condition
      };
    },
    [query]
  );

  return (
    <>
      {/* <Navbar movies={movies} /> */}
      <Navbar>
        {/* Fixing prop drilling by component composition using children in navbar */}
        <Search query={query} setQuery={setQuery} /> {/*reused*/}
        <NumResults movies={movies} />
      </Navbar>
      <Main>
        <Box>
          {" "}
          {/**left box */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />}{" "} */}
          {/** Prop drilling */}
        </Box>

        <Box>
          {" "}
          {/**right box */}
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched} //MovieDetails ko pass kia taaki movieDetails isko change kr ske aur isko watchedMoviesList mein reflect kr ske hum
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}
